const DICTS: Record<Lang, Dict> = { en, pt };

/** Translate a key for a language; falls back to English, then the key itself. `{var}` interpolation. */
export function t(lang: Lang, key: string, vars?: Record<string, string | number>): string {
  let s = DICTS[lang]?.[key] ?? en[key] ?? key;
  // Replacer FUNCTION so a `$` in the value (e.g. a folder/repo name like `$tmp`) stays literal instead of
  // being read as a `$1`/`$&`/`$$` String.replace pattern.
  if (vars) for (const [k, v] of Object.entries(vars)) s = s.replace(new RegExp(`\\{${k}\\}`, "g"), () => String(v));
  return s;
}

/** Coerce any stored/cookie value to a supported Lang (default English). */
export function normalizeLang(v?: string | null): Lang {
  if (!v) return "en";
  const s = v.toLowerCase();
  return s === "pt" || s.startsWith("pt-") || s.startsWith("port") ? "pt" : "en";
}
